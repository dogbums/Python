number = int(input("Please a whole number between 1 and 7: "))

if number == 1:
    print("One for sorrow")

if number == 2:
    print("Two for joy")

if number == 3:
    print("Three for a girl")

if number == 4:
    print("Four for a boy")

if number == 5:
    print("Five for silver")

if number == 6:
    print("Six for gold")

if number == 7:
    print("Seven for a secret")

else:
    print("Not a permitted number")
